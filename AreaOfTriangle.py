# Area of triangle = 1/2 x base x height

print("\nPlease give the measurements in centi-meters(cm)")
height= float(input("\nWhat is the height of the triangle?\n")) 
base = float(input("\nWhat is the base of the triangle?\n"))

areaOfT= 1/2 * base * height

print("\nThe area of triangle of the given height=",height, "& base=",base,"is equal to:",areaOfT)